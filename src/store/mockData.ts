// Helper to get mock data for a workspace
export const getMockDataForWorkspace = (workspaceId: string): WorkspaceMockData => {
  const data = mockData[workspaceId];
  if (!data) {
    console.warn(`No mock data found for workspace: ${workspaceId}. Using Personal as fallback.`);
    return mockData['Personal'];
  }
  return data;
};

// Helper to check if workspace has mock data
export const hasMockData = (workspaceId: string): boolean => {
  return workspaceId in mockData;
};

